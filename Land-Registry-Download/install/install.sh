# cp "$BINARY_LOCAL_PATH_ROOT/$PYTHON_NAME_MONTHLY_UPDATE_DATABASE_UPDATER" "$BINARY_INSTALL_PATH_ROOT/"
# cp "$BINARY_LOCAL_PATH_ROOT/$PYTHON_NAME_MONTHLY_UPDATE_GARBAGE_COLLECTOR" "$BINARY_INSTALL_PATH_ROOT/"
for i in "${!PYTHON_NAMES[@]}"; do
    PYTHON_NAME="${PYTHON_NAMES[$i]}"
    echo "installing $PYTHON_NAME to $BINARY_INSTALL_PATH_ROOT"
    cp "$BINARY_LOCAL_PATH_ROOT/$PYTHON_NAME" "$BINARY_INSTALL_PATH_ROOT/"
done

# Ensure python executables are executable
# chmod +x "$BINARY_INSTALL_PATH_ROOT/$PYTHON_NAME_CRON_TRIGGER"
# chmod +x "$BINARY_INSTALL_PATH_ROOT/$PYTHON_NAME_COMPLETE_DOWNLOADER"
# chmod +x "$BINARY_INSTALL_PATH_ROOT/$PYTHON_NAME_MONTHLY_UPDATE_DOWNLOADER"
# chmod +x "$BINARY_INSTALL_PATH_ROOT/$PYTHON_NAME_MONTHLY_UPDATE_SHA_CALCULATOR"
# chmod +x "$BINARY_INSTALL_PATH_ROOT/$PYTHON_NAME_MONTHLY_UPDATE_DATA_DECISION"
# chmod +x "$BINARY_INSTALL_PATH_ROOT/$PYTHON_NAME_MONTHLY_UPDATE_DATABASE_UPDATER"
# chmod +x "$BINARY_INSTALL_PATH_ROOT/$PYTHON_NAME_MONTHLY_UPDATE_GARBAGE_COLLECTOR"
for i in "${!PYTHON_NAMES[@]}"; do
    PYTHON_NAME="${PYTHON_NAMES[$i]}"
    echo "chmod +x $PYTHON_NAME"
    chmod +x "$BINARY_INSTALL_PATH_ROOT/$PYTHON_NAME"
done

# Copy systemd service files
# cp "$SERVICE_FILE_LOCAL_PATH_ROOT/$SERVICE_FILE_NAME_CRON_TRIGGER" "$SYSTEMD_INSTALL_PATH_ROOT/"
# cp "$SERVICE_FILE_LOCAL_PATH_ROOT/$SERVICE_FILE_NAME_COMPLETE_DOWNLOADER" "$SYSTEMD_INSTALL_PATH_ROOT/"
# cp "$SERVICE_FILE_LOCAL_PATH_ROOT/$SERVICE_FILE_NAME_MONTHLY_UPDATE_DOWNLOADER" "$SYSTEMD_INSTALL_PATH_ROOT/"
# cp "$SERVICE_FILE_LOCAL_PATH_ROOT/$SERVICE_FILE_NAME_MONTHLY_UPDATE_SHA_CALCULATOR" "$SYSTEMD_INSTALL_PATH_ROOT/"
# cp "$SERVICE_FILE_LOCAL_PATH_ROOT/$SERVICE_FILE_NAME_MONTHLY_UPDATE_DATA_DECISION" "$SYSTEMD_INSTALL_PATH_ROOT/"
# cp "$SERVICE_FILE_LOCAL_PATH_ROOT/$SERVICE_FILE_NAME_MONTHLY_UPDATE_DATABASE_UPDATER" "$SYSTEMD_INSTALL_PATH_ROOT/"
# cp "$SERVICE_FILE_LOCAL_PATH_ROOT/$SERVICE_FILE_NAME_MONTHLY_UPDATE_GARBAGE_COLLECTOR" "$SYSTEMD_INSTALL_PATH_ROOT/"
echo "installing $SERVICE_TARGET_FILE_NAME to $SYSTEMD_INSTALL_PATH_ROOT"
cp "$SERVICE_FILE_LOCAL_PATH_ROOT/$SERVICE_TARGET_FILE_NAME" "$SYSTEMD_INSTALL_PATH_ROOT/"

for i in "${!SERVICE_FILE_NAMES[@]}"; do
    SERVICE_FILE_NAME="${SERVICE_FILE_NAMES[$i]}"
    echo "installing $SERVICE_FILE_NAME to $SYSTEMD_INSTALL_PATH_ROOT"
    cp "$SERVICE_FILE_LOCAL_PATH_ROOT/$SERVICE_FILE_NAME" "$SYSTEMD_INSTALL_PATH_ROOT/"
done

# Create log directory
echo "create log directory $LOG_FILE_INSTALL_PATH_ROOT"
mkdir -p "$LOG_FILE_INSTALL_PATH_ROOT"
chown propertyprice:propertyprice "$LOG_FILE_INSTALL_PATH_ROOT"
chmod 755 "$LOG_FILE_INSTALL_PATH_ROOT"

# Reload systemctl to pick up new service files
echo "reload systemctl"
systemctl daemon-reload

