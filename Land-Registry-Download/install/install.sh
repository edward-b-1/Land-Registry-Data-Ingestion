NAME_CRON_TRIGGER="land_registry_cron_trigger"
NAME_COMPLETE_DOWNLOADER="land_registry_complete_downloader"
NAME_MONTHLY_UPDATE_DOWNLOADER="land_registry_monthly_update_downloader"
